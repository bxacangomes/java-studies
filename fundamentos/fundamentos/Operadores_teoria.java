package fundamentos;

public class Operadores_teoria {
    public static void main(String[] args) {
        // + - / *
        int numeroUm = 5;
        int numeroDois = 15;
        int resultado = numeroDois+numeroUm;
        System.out.println("O valor é: " + resultado);

        // %
        int resto = 53%2;
        System.out.println(resto);

        // < > <= >= == (comparação) != ----- sempre retornarão valores booleanos
        boolean isDezMaiorqueVinte = 10>20;
        System.out.println(isDezMaiorqueVinte);



    }
}
